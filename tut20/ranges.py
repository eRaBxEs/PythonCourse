# i here represents the counter
for i in range(4):
    print(i)

print()

for i in range(1, 4):
    print(i)

print()

for i in range(4, 11, 2):
    print(i)

print()

# not interested in using the value of i the counter
for _ in range(4, 11, 2):
    print(_)

print()

